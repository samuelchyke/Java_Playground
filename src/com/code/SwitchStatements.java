package com.code;

public class SwitchStatements {

    public static void main(String[] args) {

        String gender = "f";

        switch (gender){
            case "f":
                System.out.println("I am female");
                break;
            case "m":
                System.out.println("I am male");
                break;
            default:
                System.out.println("poof");

        }

    }


}
